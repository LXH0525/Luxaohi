std::shared_ptr<语法树节点> 语法分析器::分析数组声明() {
    // 语法: 数组 名称[大小] = { ... }
    消耗(词法分析器::数组关键字, "刘小黑[预期'数组'关键字呐喵~]");
    auto 节点 = std::make_shared<数组声明节点>();
    节点->名称 = 消耗(词法分析器::标识符, "刘小黑[预期数组名喵~]").值;
    消耗(词法分析器::左中括号, "刘小黑[预期一个'['呢喵~]");
    // 大小为数字或表达式
    std::stringstream 大小流;
    while (!匹配(词法分析器::右中括号)) { auto t = 消耗(); 大小流 << t.值; }
    消耗(词法分析器::右中括号, "刘小黑[预期一个']'呢喵~]");
    节点->大小 = std::stoul(大小流.str());
    消耗(词法分析器::等号, "刘小黑[预期'='呢喵~]");
    // 读取初始化列表
    消耗(词法分析器::左花括号, "刘小黑[预期一个'{'喵~]");
    while (!匹配(词法分析器::右花括号)) {
        if (匹配(词法分析器::分号) || 匹配(词法分析器::换行符)) { 消耗(); continue; }
        if (匹配(词法分析器::逗号)) { 消耗(); continue; }
        if (匹配(词法分析器::字符串) || 匹配(词法分析器::数字) ) {
            auto t = 消耗(); 节点->初始值.push_back(t.值);
        }
        else if (匹配(词法分析器::标识符)) {
            auto t = 消耗(); 节点->初始值.push_back(t.值);
        }
        else 消耗();
    }
    消耗(词法分析器::右花括号, "刘小黑[预期一个'}'喵~]");
    return 节点;
}

std::shared_ptr<条件语句节点> 语法分析器::分析如果语句() {
    auto 根节点 = std::make_shared<条件语句节点>();

    消耗(词法分析器::如果关键字, "刘小黑[预期'如果'...]");
    std::stringstream 条件流;
    while (!匹配(词法分析器::那么关键字) && !匹配(词法分析器::结束)) {
        auto tk = 消耗(); 条件流 << tk.值;
    }
    根节点->条件 = 条件流.str();
    消耗(词法分析器::那么关键字, "刘小黑[预期'那么'分支...]");
    消耗(词法分析器::左花括号, "刘小黑[需要 '{' 来开始分支]");
    while (!匹配(词法分析器::右花括号)) {
        if (匹配(词法分析器::换行符) || 匹配(词法分析器::分号)) { 消耗(); continue; }
        if (匹配(词法分析器::如果关键字)) 根节点->那么分支.push_back(分析如果语句());
        else if (匹配(词法分析器::变量关键字)) 根节点->那么分支.push_back(分析变量赋值());
        else if (匹配(词法分析器::返回关键字)) 根节点->那么分支.push_back(分析返回());
        else if (检查标识符后有左括号()) 根节点->那么分支.push_back(分析调用());
        else 消耗();
    }
    消耗(词法分析器::右花括号, "刘小黑[分支缺少结束用的'}']");

    // 支持多个 '或者' 作为 elif
    while (匹配(词法分析器::或者关键字)) {
        消耗(词法分析器::或者关键字, "刘小黑[预期'或者'关键字]");
        std::stringstream elif条件;
        while (!匹配(词法分析器::那么关键字) && !匹配(词法分析器::结束)) {
            auto tk = 消耗(); elif条件 << tk.值;
        }
        auto elifNode = std::make_shared<条件语句节点>();
        elifNode->条件 = elif条件.str();
        消耗(词法分析器::那么关键字, "刘小黑[预期'那么'来开始'或者'分支]");
        消耗(词法分析器::左花括号, "刘小黑[需要 '{' 来开始或者分支]");
        while (!匹配(词法分析器::右花括号)) {
            if (匹配(词法分析器::换行符) || 匹配(词法分析器::分号)) { 消耗(); continue; }
            if (匹配(词法分析器::如果关键字)) elifNode->那么分支.push_back(分析如果语句());
            else if (匹配(词法分析器::变量关键字)) elifNode->那么分支.push_back(分析变量赋值());
            else if (匹配(词法分析器::返回关键字)) elifNode->那么分支.push_back(分析返回());
            else if (检查标识符后有左括号()) elifNode->那么分支.push_back(分析调用());
            else 消耗();
        }
        消耗(词法分析器::右花括号, "刘小黑[或者分支缺少结束用的'}']");
        根节点->否则分支.push_back(elifNode);
    }

    // 否则分支
    if (匹配(词法分析器::否则关键字)) {
        消耗(词法分析器::否则关键字, "刘小黑[预期'否则'关键字]");
        if (匹配(词法分析器::如果关键字)) {
            根节点->否则分支.push_back(分析如果语句());
        } else {
            消耗(词法分析器::左花括号, "刘小黑[否则需要 '{' 来开始代码块]");
            while (!匹配(词法分析器::右花括号)) {
                if (匹配(词法分析器::换行符) || 匹配(词法分析器::分号)) { 消耗(); continue; }
                if (匹配(词法分析器::如果关键字)) 根节点->否则分支.push_back(分析如果语句());
                else if (匹配(词法分析器::变量关键字)) 根节点->否则分支.push_back(分析变量赋值());
                else if (匹配(词法分析器::返回关键字)) 根节点->否则分支.push_back(分析返回());
                else if (检查标识符后有左括号()) 根节点->否则分支.push_back(分析调用());
                else 消耗();
            }
            消耗(词法分析器::右花括号, "刘小黑[否则分支缺少结束用的'}']");
        }
    }

    return 根节点;
}

