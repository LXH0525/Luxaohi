std::shared_ptr<当循环节点> 语法分析器::分析当循环() {
    auto 当节点 = std::make_shared<当循环节点>();
    消耗(词法分析器::当关键字, "刘小黑[预期'当'...]");

    // 解析条件表达式
    std::stringstream 条件流;
    while (!匹配(词法分析器::那么关键字) && !匹配(词法分析器::结束)) {
        auto 令牌 = 消耗(); 条件流 << 令牌.值;
    }
    当节点->条件 = 条件流.str();

    消耗(词法分析器::那么关键字, "刘小黑[预期'那么'分支呢]");
    消耗(词法分析器::左花括号, "刘小黑[" + 全局用户名 + "，当循环后面需要加上一个'{'来开始代码块喵]");

    // 解析循环体
    while (!匹配(词法分析器::右花括号)) {
        if (匹配(词法分析器::换行符) || 匹配(词法分析器::分号)) {
            消耗();
            continue;
        }
        if (匹配(词法分析器::如果关键字)) 当节点->循环体.push_back(分析如果语句());
        else if (匹配(词法分析器::变量关键字)) 当节点->循环体.push_back(分析变量赋值());
        else if (匹配(词法分析器::返回关键字)) 当节点->循环体.push_back(分析返回());
        else if (匹配(词法分析器::当关键字)) 当节点->循环体.push_back(分析当循环());
        else if (检查标识符后有左括号()) 当节点->循环体.push_back(分析调用());
        else 消耗();
    }
    消耗(词法分析器::右花括号, "刘小黑[" + 全局用户名 + "，当循环结束的时候需要加上一个'}'来结束代码块喵]");

    return 当节点;
}

